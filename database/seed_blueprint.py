async def seed_blueprint_plan() -> None:
    """Idempotently create the Blueprint project, tasks, tags and reminders."""
    async with async_session_factory() as session:
        try:
            # Ensure user exists
            user = await session.get(User, USER_ID)
            if user is None:
                user = User(
                    user_id=USER_ID,
                    username=USERNAME,
                    first_name=FIRST_NAME,
                    timezone="Europe/Moscow",
                    language="ru",
                )
                session.add(user)
                await session.flush()
                logger.info("Seed: created user %d", USER_ID)

            # Skip if project already exists
            existing = await session.execute(
                select(Project).where(
                    Project.user_id == USER_ID,
                    Project.name == PROJECT_NAME,
                )
            )
            if existing.scalar_one_or_none() is not None:
                logger.info("Seed: Blueprint project already exists, skipping")
                return

            # Create project
            project = Project(
                user_id=USER_ID,
                name=PROJECT_NAME,
                description=PROJECT_DESC,
                color_emoji=PROJECT_EMOJI,
                start_date=PROJECT_START,
                end_date=PROJECT_END,
            )
            session.add(project)
            await session.flush()
            logger.info("Seed: created Blueprint project id=%d", project.project_id)

            now = datetime.now()
            task_count = 0
            reminder_count = 0

            for spec in TASKS:
                task = Task(
                    user_id=USER_ID,
                    project_id=project.project_id,
                    title=spec["title"],
                    description=spec.get("description"),
                    priority=spec["priority"],
                    status=TaskStatus.ACTIVE,
                    due_date=spec.get("due_date"),
                )
                session.add(task)
                await session.flush()
                task_count += 1

                for tag_name in spec.get("tags", []):
                    session.add(
                        TaskTag(
                            user_id=USER_ID,
                            task_id=task.task_id,
                            tag_name=tag_name,
                        )
                    )

                due = spec.get("due_date")
                if due:
                    for hours_before in [24, 1]:
                        reminder_time = due - timedelta(hours=hours_before)
                        if reminder_time > now:
                            session.add(
                                Reminder(
                                    user_id=USER_ID,
                                    task_id=task.task_id,
                                    reminder_time=reminder_time,
                                    reminder_type=ReminderType.BEFORE_DUE,
                                    is_sent=False,
                                )
                            )
                            reminder_count += 1

            await session.commit()
            logger.info(
                "Seed complete: %d tasks, %d reminders for project '%s'",
                task_count, reminder_count, PROJECT_NAME,
            )
        except Exception:
            await session.rollback()
            logger.exception("Seed failed")
            raise
